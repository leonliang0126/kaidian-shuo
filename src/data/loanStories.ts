/** 从数组中等概率抽一条；支持注入确定性 rng 便于测试。 */
export function pickStory(arr: readonly string[], rng: RNG = Math.random): string {
  return arr[Math.floor(rng() * arr.length) % arr.length];
}

/**
 * 把故事模板里的 {amount} 占位符替换为 fmtMoney(amount)。
 * 用于危机借款成功文案，使"文案说的金额"与"实际到账金额"严格一致。
 */
export function fillStory(template: string, amount: number): string {
  return template.replace(/\{amount\}/g, fmtMoney(amount));
}
