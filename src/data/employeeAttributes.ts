/** 按概率抽取属性（关系户 5%，其它均匀分布） */
export function drawAttribute(rng: () => number): EmployeeAttribute {
  const roll = rng();
  if (roll < 0.05) return 'guanxi_hire'; // 5% 关系户
  // 剩余的 95% 在其它 7 种中均匀分布
  const others: EmployeeAttribute[] = [
    'super_worker',
    'old_smooth',
    'slacker',
    'rookie',
    'all_rounder',
    'troublemaker',
    'social_butterfly',
  ];
  // 将 0.05-1.0 映射到 0-7 的索引
  const idx = Math.floor(((roll - 0.05) / 0.95) * others.length);
  return others[Math.min(idx, others.length - 1)];
}
